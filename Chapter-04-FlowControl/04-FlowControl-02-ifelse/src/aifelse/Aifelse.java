package aifelse;

public class Aifelse {
	
	public static void main(String args[]) {
		int math = 100;
		int english = 100;
		if (math > 60) {
			System.out.println("数学及格");
		} else {
			System.out.println("数学没及格");
		}
		if (english > 60) {
			System.out.println("英语及格");
		} else {
			System.out.println("英语没及格");
		}
	}
	
}